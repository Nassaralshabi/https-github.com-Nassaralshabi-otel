        $request = $request->all();
        $subscrided = Mail::create($request);
        
        if ($subscrided) {
            Alert::success('نجاح', 'شكرا لتسجيلك في قائمتنا البريدية');
            return back();
        }
        else {
            Alert::error('فشل', 'لم يتم تسجيلك برجاء المحاولة مرة أخري ');
            return back();
        }
        return redirect()->route('Subscribe');
    }

